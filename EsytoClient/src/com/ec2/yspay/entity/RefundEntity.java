package com.ec2.yspay.entity;

/**
 * 退款详情类
 * 
 * @author   罗洪祥
 * @version  V001Z0001
 * @date     2015年9月22日
 * @see  [相关类/方法]
 * @since  [产品/模块版本]
 */
public class RefundEntity
{
    private String orderId;
    private String refundAmount;
    private String refundReason;
    private String storeId;
    private String terminalId;
    private String opUserId;
    /**
     * 获取 orderId
     * @return 返回 orderId
     */
    public String getOrderId()
    {
        return orderId;
    }
    /**
     * 设置 orderId
     * @param 对orderId进行赋值
     */
    public void setOrderId(String orderId)
    {
        this.orderId = orderId;
    }
    /**
     * 获取 refundAmount
     * @return 返回 refundAmount
     */
    public String getRefundAmount()
    {
        return refundAmount;
    }
    /**
     * 设置 refundAmount
     * @param 对refundAmount进行赋值
     */
    public void setRefundAmount(String refundAmount)
    {
        this.refundAmount = refundAmount;
    }
    /**
     * 获取 refundReason
     * @return 返回 refundReason
     */
    public String getRefundReason()
    {
        return refundReason;
    }
    /**
     * 设置 refundReason
     * @param 对refundReason进行赋值
     */
    public void setRefundReason(String refundReason)
    {
        this.refundReason = refundReason;
    }
    /**
     * 获取 storeId
     * @return 返回 storeId
     */
    public String getStoreId()
    {
        return storeId;
    }
    /**
     * 设置 storeId
     * @param 对storeId进行赋值
     */
    public void setStoreId(String storeId)
    {
        this.storeId = storeId;
    }
    /**
     * 获取 terminalId
     * @return 返回 terminalId
     */
    public String getTerminalId()
    {
        return terminalId;
    }
    /**
     * 设置 terminalId
     * @param 对terminalId进行赋值
     */
    public void setTerminalId(String terminalId)
    {
        this.terminalId = terminalId;
    }
    /**
     * 获取 opUserId
     * @return 返回 opUserId
     */
    public String getOpUserId()
    {
        return opUserId;
    }
    /**
     * 设置 opUserId
     * @param 对opUserId进行赋值
     */
    public void setOpUserId(String opUserId)
    {
        this.opUserId = opUserId;
    }
    /** 
     * <默认构造函数>
     */
    public RefundEntity(String orderId, String refundAmount)
    {
        super();
        this.orderId = orderId;
        this.refundAmount = refundAmount;
    }
    /** 
     * <默认构造函数>
     */
    public RefundEntity(String orderId, String refundAmount, String refundReason, String storeId, String terminalId,
        String opUserId)
    {
        super();
        this.orderId = orderId;
        this.refundAmount = refundAmount;
        this.refundReason = refundReason;
        this.storeId = storeId;
        this.terminalId = terminalId;
        this.opUserId = opUserId;
    }
    

}
